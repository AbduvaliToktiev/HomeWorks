package month2.Lesson20;

public class Operators {
    private double zvonok;
    private double CMC;
    private int internet;

    public Operators() {

    }

    public Operators(double zvonok, double CMC, int internet) {
        this.zvonok = zvonok;
        this.CMC = CMC;
        this.internet = internet;
    }

    public double getZvonok() {
        return zvonok;
    }

    public void setZvonok(double zvonok) {
        this.zvonok = zvonok;
    }

    public double getCMC() {
        return CMC;
    }

    public void setCMC(double CMC) {
        this.CMC = CMC;
    }

    public int getInternet() {
        return internet;
    }

    public void setInternet(int internet) {
        this.internet = internet;
    }

}
